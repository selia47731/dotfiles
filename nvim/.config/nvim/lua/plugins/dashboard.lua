return {
  'nvimdev/dashboard-nvim',
  event = 'VimEnter',
  dependencies = 'nvim-tree/nvim-web-devicons',
  config = function()
    require('dashboard').setup {
      theme = 'hyper', -- hyperテーマを使用
      hide = {
        statusline = true, -- ステータスラインを非表示
        tabline = false, -- タブラインは表示
        winbar = false -- Winbarは表示
      },
      config = {
        header = neovim_ascii_art, -- Neovimのアスキーアートを設定
      }
    }

    local term_program = vim.env.TERM_PROGRAM
    if term_program == "iTerm.app" then
      -- sakuraテーマの色をDashboardのハイライトに統一
      local sakura_colors = require("lush_theme.sakura")
      vim.api.nvim_set_hl(0, "DashboardHeader", { fg = sakura_colors.Function.fg.hex })
      vim.api.nvim_set_hl(0, "DashboardCenter", { fg = sakura_colors.Keyword.fg.hex })
      vim.api.nvim_set_hl(0, "DashboardFooter", { fg = sakura_colors.Comment.fg.hex })
      vim.api.nvim_set_hl(0, "DashboardShortCut", { fg = sakura_colors.String.fg.hex })
    end
  end,
}
